
function suma(a, b){
    return parseInt(a) + parseInt(b);
}

function resta(a,b){
    return parseInt(a) - parseInt(b);
}

function multiplicacion(a,b){
    return parseInt(a) * parseInt(b);
}

function division(a,b){
    return parseInt(a) / parseInt(b);
}

export {suma, resta, multiplicacion, division}